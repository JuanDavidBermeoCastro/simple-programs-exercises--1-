#Escriba un programa que calcule el promedio de 4 notas ingresadas por el usuario:

#Primera nota: 55
#Segunda nota: 71
#Tercera nota: 46
#Cuarta nota: 87
#El promedio es: 64.75

#le pido al usuario que ingrese tres valores 
nota1= float(input("please write the first note: "))
nota2= float(input("please write the second note: "))
nota3= float(input("please write the third note: "))
nota4= float(input("please write the fourth note: "))
#calcular el promedio 
promedio = (nota1 + nota2 + nota3 + nota4) /4
# le doy una respuesta despues del proceso salida 
print(f"the grade average is:  {promedio} ")



